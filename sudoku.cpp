int s[9][9][10];
int i,j,k;

void read (int zt[9][9])
{
    for (i=0; i<9; i++) 
    {
        for (j=0; j<9; j++) 
        {
            s[i][j][0] = zt[i][j];

            for (k=1; k<10; k++) 
            {
                s[i][j][k] = k;
            }
        }
    }
}


